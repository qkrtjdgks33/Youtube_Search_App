async function fetchOptimizedVideos(
  maxResults: number = CONSTANTS.DEFAULT_MAX_RESULTS,
  searchQuery: string = "",
  isGenre: boolean = false
): Promise<YouTubeVideo[]> {
  const requestKey = `${isGenre ? "genre" : "search"}_${searchQuery}`;

  // Ï∫êÏãú ÌôïÏù∏
  const cached = videoCache.get(searchQuery, isGenre);
  if (cached) {
    return cached;
  }

  // ÏßÑÌñâ Ï§ëÏù∏ ÎèôÏùºÌïú ÏöîÏ≤≠Ïù¥ ÏûàÎã§Î©¥ Ï∑®ÏÜå
  if (ongoingRequests.has(requestKey)) {
    ongoingRequests.get(requestKey)?.abort();
    ongoingRequests.delete(requestKey);
  }

  // ÏÉàÎ°úÏö¥ AbortController ÏÉùÏÑ±
  const abortController = new AbortController();
  ongoingRequests.set(requestKey, abortController);

  try {
    const token = localStorage.getItem("ytAccessToken");
    if (!token) {
      throw new Error("YouTube Ïï°ÏÑ∏Ïä§ ÌÜ†ÌÅ∞Ïù¥ ÏóÜÏäµÎãàÎã§");
    }

    console.log(
      `üéµ API Ìò∏Ï∂ú ÏãúÏûë: ${searchQuery} (${isGenre ? "Ïû•Î•¥" : "Í≤ÄÏÉâ"})`
    );

    // 1) search APIÎ°ú Í∏∞Î≥∏ Ï†ïÎ≥¥ Í∞ÄÏ†∏Ïò§Í∏∞
    const searchRes = await fetch(
      `${API_CONFIG.BASE_URL}/search?part=snippet&type=video&videoCategoryId=${
        API_CONFIG.CATEGORY_ID
      }&q=${encodeURIComponent(
        searchQuery
      )}&maxResults=${maxResults}&safeSearch=${API_CONFIG.SAFE_SEARCH}`,
      {
        headers: { Authorization: `Bearer ${token}` },
        signal: abortController.signal,
      }
    );

    if (!searchRes.ok) {
      throw new Error(`Search API ÏöîÏ≤≠ Ïã§Ìå®: ${searchRes.status}`);
    }

    const searchData = await searchRes.json();
    const items: YouTubeVideo[] = searchData.items || [];

    if (!items.length) {
      console.log(`‚úÖ ${searchQuery} Í≤ÄÏÉâ ÏôÑÎ£å: Í≤∞Í≥º ÏóÜÏùå`);
      return [];
    }

    // 2) videos APIÎ°ú ÏÉÅÏÑ∏ Ï†ïÎ≥¥(Ï°∞ÌöåÏàò, ÏòÅÏÉÅ Í∏∏Ïù¥ Îì±) Í∞ÄÏ†∏Ïò§Í∏∞
    const videoIds = items.map((item: any) => item.id.videoId).join(",");
    const videosRes = await fetch(
      `${API_CONFIG.BASE_URL}/videos?part=statistics,contentDetails&id=${videoIds}`,
      {
        headers: { Authorization: `Bearer ${token}` },
        signal: abortController.signal,
      }
    );

    if (!videosRes.ok) {
      throw new Error(`Videos API ÏöîÏ≤≠ Ïã§Ìå®: ${videosRes.status}`);
    }

    const videosData = await videosRes.json();
    const videoDetails = videosData.items || [];

    // 3) Í≤ÄÏÉâ Í≤∞Í≥ºÏôÄ ÏÉÅÏÑ∏ Ï†ïÎ≥¥ Î≥ëÌï©
    const enrichedItems = items.map((item: any) => {
      const videoId = item.id.videoId;
      const details = videoDetails.find((v: any) => v.id === videoId);
      return {
        ...item,
        contentDetails: details?.contentDetails,
        statistics: details?.statistics,
      };
    });

    // Ï∫êÏãúÏóê Ï†ÄÏû•
    videoCache.set(searchQuery, enrichedItems, isGenre);
    console.log(
      `‚úÖ ${searchQuery} Í≤ÄÏÉâ ÏôÑÎ£å: ${enrichedItems.length}Í∞ú ÏòÅÏÉÅ (Ï∫êÏãúÎê®)`
    );

    return enrichedItems;
  } catch (error) {
    if (error instanceof Error) {
      if (error.name === "AbortError") {
        console.log(`üö´ ÏöîÏ≤≠ Ï∑®ÏÜåÎê®: ${searchQuery}`);
        return [];
      }
      console.error(`‚ùå ${searchQuery} Í≤ÄÏÉâ Ïã§Ìå®:`, error.message);
      throw error;
    }
    throw new Error("Ïïå Ïàò ÏóÜÎäî Ïò§Î•òÍ∞Ä Î∞úÏÉùÌñàÏäµÎãàÎã§");
  } finally {
    ongoingRequests.delete(requestKey);
  }
}

